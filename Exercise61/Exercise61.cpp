int main() {
    int N;
    cin >> N;
    vector <int> a;

    for(int i = 1; i * i <= N; i++)
        a.push_back(i * i);

    vector <int> dp(N + 1);

    for(int i = 1; i <= N; i++){
        dp[i] = dp[i - 1] + 1;
        for(int j = 1; j <= a.size(); j++){
            if(a[j] > i) break;
            dp[i] = min(dp[i], dp[i - a[j]] + 1);
        }
    }

    cout << dp[N];
    return 0;
}
